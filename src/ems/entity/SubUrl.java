package ems.entity;

public class SubUrl {

	private Integer roleId;
	private	Integer urlId;
	private	Integer subUrlId;
	private String subUrl;
	private String subUrlDesc;
	public Integer getRoleId() {
		return roleId;
	}
	public void setRoleId(Integer roleId) {
		this.roleId = roleId;
	}
	public Integer getUrlId() {
		return urlId;
	}
	public void setUrlId(Integer urlId) {
		this.urlId = urlId;
	}
	public Integer getSubUrlId() {
		return subUrlId;
	}
	public void setSubUrlId(Integer subUrlId) {
		this.subUrlId = subUrlId;
	}
	public String getSubUrl() {
		return subUrl;
	}
	public void setSubUrl(String subUrl) {
		this.subUrl = subUrl;
	}
	public String getSubUrlDesc() {
		return subUrlDesc;
	}
	public void setSubUrlDesc(String subUrlDesc) {
		this.subUrlDesc = subUrlDesc;
	}
	
	
}
